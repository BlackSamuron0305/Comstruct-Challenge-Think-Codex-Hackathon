// ── Helper ──────────────────────────────────────────────────────────
String t(BuildContext context, String key) {
  final lang = context.watch<LanguageCubit>().state;
  return _translations[lang]?[key] ?? _translations['en']?[key] ?? key;
}

/// Read-only variant that doesn't subscribe to rebuilds.
String tRead(BuildContext context, String key) {
  final lang = context.read<LanguageCubit>().state;
  return _translations[lang]?[key] ?? _translations['en']?[key] ?? key;
}

